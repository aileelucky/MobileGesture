package com.shinemo.shinemogesture.model;

public class QueryGestureRequest {
    private String account;
    private String biologyInfo;
    private String uid;

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getBiologyInfo() {
        return biologyInfo;
    }

    public void setBiologyInfo(String biologyInfo) {
        this.biologyInfo = biologyInfo;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }


}
