package com.shinemo.shinemogesture.model;

public class SettingGestureRequest {

    private String token;
    private String biologyInfo;
    private String ticket;

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getBiologyInfo() {
        return biologyInfo;
    }

    public void setBiologyInfo(String biologyInfo) {
        this.biologyInfo = biologyInfo;
    }

    public String getTicket() {
        return ticket;
    }

    public void setTicket(String ticket) {
        this.ticket = ticket;
    }
}
